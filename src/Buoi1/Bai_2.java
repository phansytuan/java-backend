package Buoi1;

import java.util.Scanner;

public class Bai_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("nhập vào số a: ");
        int a = sc.nextInt();
        System.out.println("nhập vào số b: ");
        int b = sc.nextInt();

        System.out.println("a+b= "+(a+b));
        System.out.println("a*b= "+(a*b));
        System.out.println("a/b= "+(a/b));
        System.out.println("a%b= "+(a%b));
    }
}
